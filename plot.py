def Plot(Ys, X, x: str, y: str):
	global i, j

	for Y in Ys:
		ax[i][j].plot(X, Y[0], label = Y[1], linestyle = '--')

	
	
	ax[i][j].legend()
	ax[i][j].set_xlabel(x)
	ax[i][j].set_ylabel(y)

	j+=1
	
	if j == 2:
		j = 0
		i += 1
	



